<script>
    var activities = ["Year End Party", "ACS Day", "General Assembly"];
    var activitiesContainer = document.getElementById("activitiesContainer");
    var showingactivities = false;

    function toggleactivities() {
      if (showingactivities) {
        hideactivities();
        showingactivities = false;
      } else {
        showactivities();
        showingactivities = true;
      }
    }

    function showactivities() {
      activitiesContainer.innerHTML = ""; // 

      var activitiesList = document.createElement("ul"); //
      for (var i = 0; i < activities.length; i++) {
        var activitiesItem = document.createElement("li"); //
        activitiesItem.textContent = activities[i];
        activitiesList.appendChild(activitiesItem);
      }

      activitiesContainer.appendChild(activitiesList);
    }

    function hideactivities() {
      activitiesContainer.innerHTML = "";  
    }
  </script>
